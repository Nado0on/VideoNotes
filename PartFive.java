public class PartFive {
    public static void main(String[] args) {
        // semicolons divide parameters into three sections
        // first parameter is a counter parameter
        // second parameter is a condition, as long as the condition is true the loop will execute
        // third condition will be executed once after every instance of loop
        // i++ is i = i + 1
        for (int i = 0; i < 5; i++) {
          //  System.out.println("Hi Bye");
            System.out.printf("The value of i is: %d", i);

        }
    }
}
